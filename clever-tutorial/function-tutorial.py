import turtle

bud_turtle = turtle.Turtle()

# This is a square
def square():
    bud_turtle.forward(100)
    bud_turtle.right(90)
    bud_turtle.forward(100)
    bud_turtle.right(90)
    bud_turtle.forward(100)
    bud_turtle.right(90)
    bud_turtle.forward(100)

square()
bud_turtle.forward(100)
square()
